<h2 id="git-常用命令" tabindex="-1"><a class="header-anchor" href="#git-常用命令" aria-hidden="true">#</a> git 常用命令</h2>
<p><strong>git init：</strong> 在现有目录中初始化一个新的 Git 仓库。
<strong>git clone：</strong> 克隆一个远程仓库到本地。
<strong>git add：</strong> 将文件的更改添加到暂存区。
<strong>git commit：</strong> 将暂存区的更改提交到本地仓库。
<strong>git push：</strong> 将本地仓库的更改推送到远程仓库。
<strong>git pull：</strong> 从远程仓库拉取更改并合并到本地仓库。
<strong>git branch：</strong> 列出、创建或删除分支。
<strong>git merge：</strong> 合并两个分支的更改。
<strong>git log：</strong> 查看提交日志。
<strong>git status：</strong> 查看工作区和暂存区的状态。
<strong>git diff：</strong> 查看工作区、暂存区或提交之间的差异。
<strong>git reset：</strong> 恢复或取消提交。
<strong>git remote：</strong> 管理远程仓库。
<strong>git fetch：</strong> 从远程仓库获取最新的更改，但不合并到本地分支。
<strong>git tag：</strong> 创建、列出或删除标签。</p>
<h2 id="git-commit提交规范" tabindex="-1"><a class="header-anchor" href="#git-commit提交规范" aria-hidden="true">#</a> git commit提交规范</h2>
<p><strong>feat:</strong> 新增功能</p>
<p><strong>fix:</strong> 修复 bug</p>
<p><strong>docs:</strong> 文档注释</p>
<p><strong>style:</strong> 代码格式调整（不影响代码运行的变动）</p>
<p><strong>refactor:</strong> 重构、优化（既不增加新功能，也不是修复 bug）</p>
<p><strong>perf:</strong> 性能优化</p>
<p><strong>test:</strong> 增加测试</p>
<p><strong>chore:</strong> 构建过程或辅助工具的变动</p>
<p><strong>revert:</strong> 回退</p>
<p><strong>build:</strong> 打包</p>
